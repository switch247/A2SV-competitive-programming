class Solution:
    def canPlaceFlowers(self, flowerbed: List[int], n: int) -> bool:
        if n == 0:
            return True
        
        for i, p in enumerate(flowerbed):
            if p == 0 and (i == 0 or flowerbed[i-1] == 0) and (i==len(flowerbed)-1 or flowerbed[i+1] == 0):
                n -= 1
                if n == 0:
                    return True
                
                flowerbed[i] = 1
                
        return False